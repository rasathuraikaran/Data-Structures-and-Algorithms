package Hashing;



public class Entry {
    private int key;
    private String value;

    public Entry(int key, String value) {	//assign key and value
        super();
        this.key = key;
        this.value = value;
    }

    public int getKey() {	//get a key
        return key;
    }

    public String getValue() {	//get a value
        return value;
    }





}


